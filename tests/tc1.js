let num = 3;
if (num % 2 === 0) {
    console.log(num + " is Even");
} else {
    console.log(num + " is Odd");
}

switch(num) {
    case 1 : 
        console.log("1 is One");
        break;
    case 2 : 
        console.log("2 is Two");
        break;
    case 3 : 
        console.log("3 is Three");
        break;
}